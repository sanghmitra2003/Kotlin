fun main()
{
    val num1 = 10
    val num2 = 20

    //  If -->

    if (num1 < num2)
    {
        println(":)")
    }

    //  If Else -->

    if (num1 > num2)
    {
        println(":)")
    }
    else
    {
        println(":(")
    }

    //  Else If -->

    if (num1 > num2)
    {
        println(":|")
    }
    else if (num1 >= 10 && num2 <=50)
    {
        println(":)")
    }
    else
    {
        println(":(")
    }
}